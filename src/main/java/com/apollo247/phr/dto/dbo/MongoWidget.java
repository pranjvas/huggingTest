package src.main.java.com.apollo247.phr.dto.dbo;

public class MongoWidget {

    private String widgetId;
    private String widgetName;
    private String widgetSize;
    private String widgetLocation;

    public String getWidgetId() {
        return widgetId;
    }

    public void setWidgetId(String widgetId) {
        this.widgetId = widgetId;
    }

    public String getWidgetName() {
        return widgetName;
    }

    public void setWidgetName(String widgetName) {
        this.widgetName = widgetName;
    }

    public String getWidgetSize() {
        return widgetSize;
    }

    public void setWidgetSize(String widgetSize) {
        this.widgetSize = widgetSize;
    }

    public String getWidgetLocation() {
        return widgetLocation;
    }

    public void setWidgetLocation(String widgetLocation) {
        this.widgetLocation = widgetLocation;
    }
}
